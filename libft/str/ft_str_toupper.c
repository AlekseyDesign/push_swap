#include "libft.h"

/*
**	Переводит строчные буквы в строке на заглавные.
**	все остальные символы (не буквы) оставляет без изменений.
**	нулевую строку игнорирует.
*/

void		ft_str_toupper(char *str)
{
	size_t	n;

	n = 0;
	if (str == NULL)
		return ;
	while (str[n])
	{
		if (str[n] >= 'a' && str[n] <= 'z')
			str[n] = (str[n] - 32);
		n++;
	}
}
